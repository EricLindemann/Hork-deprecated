public class Exercise {
    public String exerciseName;
    public Integer exerciseSets;
    public Integer exerciseReps;
    public Integer exerciseWeight;

    public Integer getExerciseWeight() {
        return exerciseWeight;
    }

    public void setExerciseWeight(Integer exerciseWeight) {
        this.exerciseWeight = exerciseWeight;
    }

    public Integer getExerciseReps() {
        return exerciseReps;
    }

    public void setExerciseReps(Integer exerciseReps) {
        this.exerciseReps = exerciseReps;
    }

    public Integer getExerciseSets() {
        return exerciseSets;
    }

    public void setExerciseSets(Integer exerciseSets) {
        this.exerciseSets = exerciseSets;
    }

    public String getExerciseName() {
        return exerciseName;
    }

    public void setExerciseName(String exerciseName) {
        this.exerciseName = exerciseName;
    }





}
